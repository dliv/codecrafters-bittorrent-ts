export const asByte = (ch: string) => ch.charCodeAt(0);

export const I = asByte('i');
export const E = asByte('e');
export const COLON = asByte(':');
export const HYPHEN = asByte('-');
export const $0 = asByte('0');
export const $9 = asByte('9');
export const L = asByte('l');
export const D = asByte('d');
